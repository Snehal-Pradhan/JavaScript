const month = 3;
switch (month) {
    case 1: 
        console.log(`JAN`);
        break;
    case 2: 
        console.log(`FEB`);
        break;
    case 3: 
        console.log(`MAR`);
        break;
    case 4: 
        console.log(`APR`);
        break;
    case 5: 
        console.log(`MAY`);
        break;
    case 6: 
        console.log(`JUNE`);
        break;
    case 7: 
        console.log(`JULY`);
        break;
    case 8: 
        console.log(`AUG`);
        break;
    case 9: 
        console.log(`SEP`);
        break;
    case 10: 
        console.log(`OCT`);
        break;
    case 11: 
        console.log(`NOV`);
        break;
    case 12: 
        console.log(`DEC`);
        break;

    default:
        console.log(`no match`);
        break;
}